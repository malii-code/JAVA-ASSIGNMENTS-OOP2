public class DescendingNumbers {
    public static void main(String[] args) {
        // Syntax: for (start; condition; decrement)
        // Starts at 100, continues while i >= 50, subtracts 1 each time
        for (int i = 100; i >= 50; i--) {
            System.out.println(i);
        }
    }
}
